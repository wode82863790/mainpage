package com.ykd.entity;

public class Blog {
	private String blog_id;
	private String blog_title;
	private String blog_date;
	private String blog_banner;
	public String getBlog_id() {
		return blog_id;
	}
	public void setBlog_id(String blog_id) {
		this.blog_id = blog_id;
	}
	public String getBlog_title() {
		return blog_title;
	}
	public void setBlog_title(String blog_title) {
		this.blog_title = blog_title;
	}
	public String getBlog_date() {
		return blog_date;
	}
	public void setBlog_date(String blog_date) {
		this.blog_date = blog_date;
	}
	public String getBlog_banner() {
		return blog_banner;
	}
	public void setBlog_banner(String blog_banner) {
		this.blog_banner = blog_banner;
	}
}
