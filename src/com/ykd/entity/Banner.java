package com.ykd.entity;

public class Banner {
	private String banner_id;
	private String banner_name;
	private String banner_src;
	private String banner_date;
	private String banner_outid;
	private String banner_url;
	public String getBanner_url() {
		return banner_url;
	}
	public void setBanner_url(String banner_url) {
		this.banner_url = banner_url;
	}
	public String getBanner_outid() {
		return banner_outid;
	}
	public void setBanner_outid(String banner_outid) {
		this.banner_outid = banner_outid;
	}
	public String getBanner_id() {
		return banner_id;
	}
	public void setBanner_id(String banner_id) {
		this.banner_id = banner_id;
	}
	public String getBanner_name() {
		return banner_name;
	}
	public void setBanner_name(String banner_name) {
		this.banner_name = banner_name;
	}
	public String getBanner_src() {
		return banner_src;
	}
	public void setBanner_src(String banner_src) {
		this.banner_src = banner_src;
	}
	public String getBanner_date() {
		return banner_date;
	}
	public void setBanner_date(String banner_date) {
		this.banner_date = banner_date;
	}
}
