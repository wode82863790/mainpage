package com.ykd.entity;

public class News {
	private String news_id;
	private String news_title;
	private String news_date;
	private String news_lit;
	private String news_inner;
	public String getNews_id() {
		return news_id;
	}
	public void setNews_id(String news_id) {
		this.news_id = news_id;
	}
	public String getNews_title() {
		return news_title;
	}
	public void setNews_title(String news_title) {
		this.news_title = news_title;
	}
	public String getNews_date() {
		return news_date;
	}
	public void setNews_date(String news_date) {
		this.news_date = news_date;
	}
	public String getNews_lit() {
		return news_lit;
	}
	public void setNews_lit(String news_lit) {
		this.news_lit = news_lit;
	}
	public String getNews_inner() {
		return news_inner;
	}
	public void setNews_inner(String news_inner) {
		this.news_inner = news_inner;
	}
}
