package com.ykd.entity;

public class Help {
	private String help_id;
	private String help_ask;
	private String help_ans;
	public String getHelp_id() {
		return help_id;
	}
	public void setHelp_id(String help_id) {
		this.help_id = help_id;
	}
	public String getHelp_ask() {
		return help_ask;
	}
	public void setHelp_ask(String help_ask) {
		this.help_ask = help_ask;
	}
	public String getHelp_ans() {
		return help_ans;
	}
	public void setHelp_ans(String help_ans) {
		this.help_ans = help_ans;
	}
}
