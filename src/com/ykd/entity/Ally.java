package com.ykd.entity;

public class Ally {
	private String ally_id;
	private 	String ally_name;
	private String ally_call;
	public String getAlly_id() {
		return ally_id;
	}
	public void setAlly_id(String ally_id) {
		this.ally_id = ally_id;
	}
	public String getAlly_name() {
		return ally_name;
	}
	public void setAlly_name(String ally_name) {
		this.ally_name = ally_name;
	}
	public String getAlly_call() {
		return ally_call;
	}
	public void setAlly_call(String ally_call) {
		this.ally_call = ally_call;
	}
}
