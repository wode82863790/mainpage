package com.ykd.entity;

public class Honor {
	private String honor_id;
	private String honor_title;
	private String honor_inner;
	private String honor_date;
	private String honor_src;
	public String getHonor_src() {
		return honor_src;
	}
	public void setHonor_src(String honor_src) {
		this.honor_src = honor_src;
	}
	public String getHonor_date() {
		return honor_date;
	}
	public void setHonor_date(String honor_date) {
		this.honor_date = honor_date;
	}
	public String getHonor_id() {
		return honor_id;
	}
	public void setHonor_id(String honor_id) {
		this.honor_id = honor_id;
	}
	public String getHonor_title() {
		return honor_title;
	}
	public void setHonor_title(String honor_title) {
		this.honor_title = honor_title;
	}
	public String getHonor_inner() {
		return honor_inner;
	}
	public void setHonor_inner(String honor_inner) {
		this.honor_inner = honor_inner;
	}
}
